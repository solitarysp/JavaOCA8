package com.lethanh98.java.learn.oca8.chap1.DeclaringInitializingVariables;

public class DeclaringAndInitializing {
    public static void main(String[] args) {
        String name; // Khai báo 1 biến name kiểu String
        int age; // khai báo 1 age kiểu int

        name = "Thành"; // Khởi tạo biến name bằng Thành
        age = 18; // khởi tạo biến age  = 18

        String address = "ha noi"; // Khai báo biến address có kiểu dữ liệu String được khởi tạo với giá trị ha noi (Hợp nhất giữa khai báo và khởi tạo biến)
    }
}
